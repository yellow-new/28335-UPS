void Init_Spwm(SPWM_VRB *Init)
{


    Init->N = 200;
    Init->M = 0.668;
    Init->Change_Flag = 0;
    Init->Pid_Flag = 0;
    Init->frequency = 50;

    Init->Period = 150000000/(2*Init->N*Init->frequency);

    EPwm2Regs.TBPRD=Init->Period-1;
    EPwm3Regs.TBPRD=Init->Period-1;

    Change_M(Init);
}



